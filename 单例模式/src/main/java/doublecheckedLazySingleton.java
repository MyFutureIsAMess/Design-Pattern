public class doublecheckedLazySingleton {
    private volatile static doublecheckedLazySingleton uniqueSingleton;

    private static doublecheckedLazySingleton getInstance(){
        if(uniqueSingleton == null){
            synchronized (doublecheckedLazySingleton.class){
                if(uniqueSingleton == null){
                    uniqueSingleton = new doublecheckedLazySingleton();
                }
            }
        }
        return uniqueSingleton;
    }
}
